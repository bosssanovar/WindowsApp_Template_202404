using Feature.DataFile;

using Microsoft.Win32;

using System.IO;

namespace FileAccessor
{
    /// <summary>
    /// 設定ファイルへのアクセスクラス
    /// </summary>
    public class DataFileAccessor : IDataFileAccessor
    {
        #region Constants -------------------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Fields ----------------------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Properties ------------------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Events ----------------------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Constructor -----------------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Methods - public ------------------------------------------------------------------------------

        /// <inheritdoc/>
        public bool Load(ref string content)
        {
            OpenFileDialog openFileDialog = new()
            {
                Filter = "Data file (*.dat)|*.dat",
            };
            if (openFileDialog.ShowDialog() == false)
            {
                return false;
            }

            content = File.ReadAllText(openFileDialog.FileName);

            return true;
        }

        /// <inheritdoc/>
        public bool Save(string content)
        {
            SaveFileDialog sfd = new()
            {
                Filter = "Data file (*.dat)|*.dat",
            };
            if (sfd.ShowDialog() == false)
            {
                return false;
            }

            File.WriteAllText(sfd.FileName, content);

            return true;
        }

        #endregion --------------------------------------------------------------------------------------------

        #region Methods - internal ----------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Methods - protected ---------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Methods - private -----------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Methods - override ----------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------

        #region Inner Class/Enum/etc. -------------------------------------------------------------------------

        #endregion --------------------------------------------------------------------------------------------
    }
}
